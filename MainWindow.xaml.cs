    private void OrderDetailsButton_Click(object sender, RoutedEventArgs e)
    {
        if (_isBusy || sender is not Button button || button.DataContext is not DataRowView rowView)
        {
            return;
        }

        var sourceValues = BabyShopRepository.GetSourceValues(rowView);
        if (!sourceValues.TryGetValue("order_id", out var rawOrderId) || rawOrderId is null || rawOrderId is DBNull)
        {
            MessageBox.Show(
                LanguageManager.CurrentLanguage == AppLanguage.Russian ? "Не удалось определить ID заказа." : "Could not determine the order ID.",
                Title,
                MessageBoxButton.OK,
                MessageBoxImage.Warning);
            return;
        }

        CustomerOrderGrid.SelectedItem = rowView;

        var orderDetailsWindow = new OrderDetailsWindow(Convert.ToInt32(rawOrderId, CultureInfo.CurrentCulture))
        {
            Owner = this
        };

        orderDetailsWindow.ShowDialog();
        e.Handled = true;
    }

