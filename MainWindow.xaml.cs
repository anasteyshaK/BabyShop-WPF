    private void CustomerOrderGrid_AutoGeneratingColumn(object? sender, DataGridAutoGeneratingColumnEventArgs e)
    {
        if (IsOrderItemsColumnName(e.PropertyName))
        {
            e.Cancel = true;
            return;
        }

        if (sender is not DataGrid)
        {
            return;
        }

        var headerText = e.Column.Header?.ToString() ?? string.Empty;
        var normalizedHeader = headerText.Trim().ToLowerInvariant();
        var normalizedProperty = e.PropertyName.Trim().ToLowerInvariant();

        if (e.Column is DataGridTextColumn textColumn)
        {
            var centeredStyle = new Style(typeof(TextBlock));
            centeredStyle.Setters.Add(new Setter(TextBlock.FontWeightProperty, FontWeights.Normal));
            centeredStyle.Setters.Add(new Setter(TextBlock.TextAlignmentProperty, TextAlignment.Left));
            centeredStyle.Setters.Add(new Setter(FrameworkElement.HorizontalAlignmentProperty, HorizontalAlignment.Left));
            centeredStyle.Setters.Add(new Setter(FrameworkElement.VerticalAlignmentProperty, VerticalAlignment.Center));
            centeredStyle.Setters.Add(new Setter(TextBlock.TextTrimmingProperty, TextTrimming.CharacterEllipsis));
            textColumn.ElementStyle = centeredStyle;
        }

        if (normalizedProperty is "no." or "пїЅ" or "№" || normalizedHeader is "№" or "no.")
        {
            e.Column.Width = new DataGridLength(64);
            e.Column.Header = "№";
            return;
        }

        if (normalizedHeader.Contains("фио") || normalizedHeader.Contains("client") || normalizedProperty.Contains("fullname"))
        {
            e.Column.Width = new DataGridLength(190);
            return;
        }

        if (normalizedHeader.Contains("адрес") || normalizedHeader.Contains("address"))
        {
            e.Column.Width = new DataGridLength(210);
            return;
        }

        if (normalizedHeader.Contains("дата начала") || normalizedHeader.Contains("start"))
        {
            e.Column.Width = new DataGridLength(190);
            return;
        }

        if (normalizedHeader.Contains("дата окончания") || normalizedHeader.Contains("end"))
        {
            e.Column.Width = new DataGridLength(190);
            return;
        }

        if (normalizedHeader.Contains("статус") || normalizedProperty.Contains("status"))
        {
            e.Column.Width = new DataGridLength(118);
            e.Column.Header = LanguageManager.CurrentLanguage == AppLanguage.Russian ? "Статус" : "Status";
            return;
        }

        if (normalizedHeader.Contains("сумма") || normalizedProperty.Contains("total_cost"))
        {
            e.Column.Width = new DataGridLength(128);
            e.Column.Header = LanguageManager.CurrentLanguage == AppLanguage.Russian ? "Сумма" : "Total";
        }
    }

