    private void ApplySearch(string query)
    {
        query = query.Trim();
        var imageColumnNames = new HashSet<string>(StringComparer.OrdinalIgnoreCase)
        {
            "Image",
            "Image Path",
            LanguageManager.GetColumnName("image_path") ?? "Image"
        };

        string BuildSearchFilter(DataView view)
        {
            if (string.IsNullOrEmpty(query))
            {
                return string.Empty;
            }

            var columns = view.Table?.Columns;
            if (columns == null || columns.Count == 0)
            {
                return string.Empty;
            }

            var escaped = EscapeRowFilterValue(query);
            var clauses = new List<string>();

            foreach (DataColumn column in columns)
            {
                if (column.ColumnName.StartsWith("__src_", StringComparison.OrdinalIgnoreCase) ||
                    column.ColumnName.StartsWith("__preview_", StringComparison.OrdinalIgnoreCase))
                {
                    continue;
                }

                if (imageColumnNames.Contains(column.ColumnName))
                {
                    continue;
                }

                clauses.Add(column.DataType == typeof(string)
                    ? $"[{column.ColumnName}] LIKE '%{escaped}%'"
                    : $"Convert([{column.ColumnName}], 'System.String') LIKE '%{escaped}%'");
            }

            return clauses.Count == 0 ? string.Empty : $"({string.Join(" OR ", clauses)})";
        }

        void Filter(DataView? view, bool includeOrderStatusFilter = false)
        {
            if (view == null)
            {
                return;
            }

            var filters = new List<string>();
            var searchFilter = BuildSearchFilter(view);
            if (!string.IsNullOrWhiteSpace(searchFilter))
            {
                filters.Add(searchFilter);
            }

            if (includeOrderStatusFilter)
            {
                var statusFilter = BuildOrderStatusFilter(view);
                if (!string.IsNullOrWhiteSpace(statusFilter))
                {
                    filters.Add(statusFilter);
                }
            }

            try
            {
                view.RowFilter = filters.Count == 0 ? string.Empty : string.Join(" AND ", filters);
            }
            catch (EvaluateException)
            {
                view.RowFilter = string.Empty;
            }
            catch (SyntaxErrorException)
            {
                view.RowFilter = string.Empty;
            }
        }

        Filter(_customerView);
        Filter(_fabricView);
        Filter(_productView);
        Filter(_orderView, includeOrderStatusFilter: true);
        Filter(_orderProductView);
    }

    private static string EscapeRowFilterValue(string value)
    {
        return value
            .Replace("'", "''")
            .Replace("[", "[[]")
            .Replace("]", "[]]")
            .Replace("%", "[%]")
            .Replace("*", "[*]");
    }

    private string BuildOrderStatusFilter(DataView view)
    {
        var selectedStatuses = new List<string>();
        if (OrderStatusPendingCheckBox.IsChecked == true)
        {
            selectedStatuses.Add("Pending");
        }

        if (OrderStatusShippedCheckBox.IsChecked == true)
        {
            selectedStatuses.Add("Shipped");
        }

        if (OrderStatusCompletedCheckBox.IsChecked == true)
        {
            selectedStatuses.Add("Completed");
        }

        if (selectedStatuses.Count == 0)
        {
            return "1 = 0";
        }

        var statusColumn = view.Table?.Columns.Contains("__src_order_status") == true
            ? "__src_order_status"
            : view.Table?.Columns.Cast<DataColumn>().FirstOrDefault(column => column.ColumnName.Contains("СЃС‚Р°С‚СѓСЃ", StringComparison.OrdinalIgnoreCase) ||
                                                                              column.ColumnName.Contains("status", StringComparison.OrdinalIgnoreCase))?.ColumnName;

        if (string.IsNullOrWhiteSpace(statusColumn))
        {
            return string.Empty;
        }

        return string.Join(" OR ", selectedStatuses.Select(status => $"[{statusColumn}] = '{status}'")) is { } clause
            ? $"({clause})"
            : string.Empty;
    }

    private void ApplyFreshOrderSort()
    {
        if (_orderView?.Table == null)
        {
            return;
        }

        if (_orderView.Table.Columns.Contains("__src_start_date"))
        {
            _orderView.Sort = _orderView.Table.Columns.Contains("__src_order_id")
                ? "[__src_start_date] DESC, [__src_order_id] DESC"
                : "[__src_start_date] DESC";
            return;
        }

        var startDateColumn = _orderView.Table.Columns
            .Cast<DataColumn>()
            .FirstOrDefault(column => column.ColumnName.Contains("РґР°С‚Р° РЅР°С‡Р°Р»Р°", StringComparison.OrdinalIgnoreCase) ||
                                      column.ColumnName.Contains("start", StringComparison.OrdinalIgnoreCase));

        if (startDateColumn is not null)
        {
            _orderView.Sort = $"[{startDateColumn.ColumnName}] DESC";
        }
    }

    private static void ApplyDefaultGridSort(string tableName, DataView view)
    {
        if (view.Table.Columns.Count == 0)
        {
            return;
        }

        var sortClause = tableName.ToUpperInvariant() switch
        {
            "PRODUCTFABRICVIEW" => BuildSortClause(view.Table, "__src_product_id"),
            "CUSTOMERORDERSVIEW" => BuildSortClause(view.Table, "__src_order_id"),
            "ORDERPRODUCTVIEW" => BuildNumberSortClause(view.Table),
            "CUSTOMERVIEW" => BuildSortClause(view.Table, "__src_customer_id"),
            "FABRICVIEW" => BuildSortClause(view.Table, "__src_fabric_id"),
            _ => string.Empty
        };

        if (string.IsNullOrWhiteSpace(sortClause))
        {
            var fallbackColumnName = view.Table.Columns[0].ColumnName;
            if (string.IsNullOrWhiteSpace(fallbackColumnName))
            {
                return;
            }

            sortClause = $"[{fallbackColumnName}] ASC";
        }

        view.Sort = sortClause;
    }

    private static string BuildNumberSortClause(DataTable table)
    {
        var numberColumn = table.Columns
            .Cast<DataColumn>()
            .FirstOrDefault(column =>
                string.Equals(column.ColumnName, "№", StringComparison.OrdinalIgnoreCase) ||
                string.Equals(column.ColumnName, "No.", StringComparison.OrdinalIgnoreCase) ||
                string.Equals(column.ColumnName, "пїЅ", StringComparison.OrdinalIgnoreCase));

        return numberColumn is null
            ? string.Empty
            : $"[{numberColumn.ColumnName}] ASC";
    }

    private static string BuildSortClause(DataTable table, params string[] columnNames)
    {
        var existingColumns = columnNames
            .Where(columnName => table.Columns.Contains(columnName))
            .Select(columnName => $"[{columnName}] ASC")
            .ToArray();

        return existingColumns.Length == 0
            ? string.Empty
            : string.Join(", ", existingColumns);
    }

