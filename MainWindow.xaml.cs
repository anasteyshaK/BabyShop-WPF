    private async Task DeleteSelectedRecordAsync(bool showSelectMessage)
    {
        if (GetCurrentGrid().SelectedItem is not DataRowView rowView)
        {
            if (showSelectMessage)
            {
                MessageBox.Show(LanguageManager.Get("SelectRowToDelete"), LanguageManager.Get("DeleteRecord"), MessageBoxButton.OK, MessageBoxImage.Information);
            }

            return;
        }

        if (GetSelectedTableName().Equals("CUSTOMER", StringComparison.OrdinalIgnoreCase))
        {
            MessageBox.Show(
                LanguageManager.Get("CustomerDeleteBlocked"),
                LanguageManager.Get("DeleteRecord"),
                MessageBoxButton.OK,
                MessageBoxImage.Information);
            return;
        }

        var confirmation = MessageBox.Show(
            LanguageManager.Get("DeleteConfirm"),
            LanguageManager.Get("DeleteRecord"),
            MessageBoxButton.YesNo,
            MessageBoxImage.Warning);

        if (confirmation != MessageBoxResult.Yes)
        {
            return;
        }

        SetBusyState(true);
        try
        {
            var sourceValues = BabyShopRepository.GetSourceValues(rowView);
            await _repository.DeleteRecordFromSourceAsync(GetSelectedTableName(), sourceValues);
            await LoadAllTablesAsync();
        }
        catch (Exception exception)
        {
            MessageBox.Show(exception.Message, LanguageManager.Get("DeleteRecord"), MessageBoxButton.OK, MessageBoxImage.Error);
        }
        finally
        {
            SetBusyState(false);
        }
    }

