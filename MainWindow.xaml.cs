    private async Task<DataView?> LoadTableIntoGridAsync(string tableName, DataGrid dataGrid, TextBlock countTextBlock)
    {
        try
        {
            var table = await _repository.GetDisplayTableAsync(tableName);
            var view = table.DefaultView;
            ApplyDefaultGridSort(tableName, view);
            dataGrid.ItemsSource = view;
            if (tableName.Equals("CUSTOMERORDERSVIEW", StringComparison.OrdinalIgnoreCase))
            {
                EnsureOrderDetailsButtonColumn(dataGrid);
            }
            countTextBlock.Text = table.Rows.Count.ToString();
            return view;
        }
        catch (Exception exception)
        {
            dataGrid.ItemsSource = null;
            countTextBlock.Text = "0";
            MessageBox.Show(
                exception.Message,
                LanguageManager.Format("LoadError", _repository.GetFriendlyTableName(tableName)),
                MessageBoxButton.OK,
                MessageBoxImage.Warning);
            return null;
        }
    }

