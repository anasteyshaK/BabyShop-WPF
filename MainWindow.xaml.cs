    private static IEnumerable<T> FindVisualChildren<T>(DependencyObject parent) where T : DependencyObject
    {
        for (var i = 0; i < VisualTreeHelper.GetChildrenCount(parent); i++)
        {
            var child = VisualTreeHelper.GetChild(parent, i);
            if (child is T typedChild)
            {
                yield return typedChild;
            }

            foreach (var nestedChild in FindVisualChildren<T>(child))
            {
                yield return nestedChild;
            }
        }
    }

    private static T? FindVisualParent<T>(DependencyObject? child) where T : DependencyObject
    {
        while (child is not null)
        {
            if (child is T typedParent)
            {
                return typedParent;
            }

            child = VisualTreeHelper.GetParent(child);
        }

        return null;
    }

    private void DataGrid_AutoGeneratingColumn(object? sender, DataGridAutoGeneratingColumnEventArgs e)
    {
        if (e.PropertyName.StartsWith("__src_", StringComparison.OrdinalIgnoreCase))
        {
            e.Cancel = true;
            return;
        }

        if (e.PropertyName.Equals("пїЅ", StringComparison.OrdinalIgnoreCase) ||
            e.PropertyName.Equals("No.", StringComparison.OrdinalIgnoreCase) ||
            e.PropertyName.Equals("№", StringComparison.OrdinalIgnoreCase) ||
            string.Equals(e.Column.Header?.ToString(), "№", StringComparison.OrdinalIgnoreCase) ||
            string.Equals(e.Column.Header?.ToString(), "No.", StringComparison.OrdinalIgnoreCase))
        {
            e.Column.Width = new DataGridLength(60);

            if (e.Column is DataGridTextColumn textColumn)
            {
                var accentBrush = TryFindResource("AccentBrush") as Brush ??
                                  TryFindResource("AccentStrongBrush") as Brush;

                if (accentBrush is not null)
                {
                    var elementStyle = new Style(typeof(TextBlock));
                    elementStyle.Setters.Add(new Setter(TextBlock.ForegroundProperty, accentBrush));
                    elementStyle.Setters.Add(new Setter(TextBlock.FontWeightProperty, FontWeights.Normal));
                    elementStyle.Setters.Add(new Setter(TextBlock.TextAlignmentProperty, TextAlignment.Left));
                    elementStyle.Setters.Add(new Setter(FrameworkElement.HorizontalAlignmentProperty, HorizontalAlignment.Left));
                    elementStyle.Setters.Add(new Setter(FrameworkElement.VerticalAlignmentProperty, VerticalAlignment.Center));
                    textColumn.ElementStyle = elementStyle;
                }
            }
        }
    }

