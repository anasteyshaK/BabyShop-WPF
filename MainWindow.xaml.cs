    private void StartAutomaticProjectBackupScheduler()
    {
        if (!_backupService.CanCreateBackup)
        {
            return;
        }

        _autoProjectBackupTimer ??= new DispatcherTimer
        {
            Interval = AutoProjectBackupCheckInterval
        };

        _autoProjectBackupTimer.Tick -= AutoProjectBackupTimer_Tick;
        _autoProjectBackupTimer.Tick += AutoProjectBackupTimer_Tick;

        if (!_autoProjectBackupTimer.IsEnabled)
        {
            _autoProjectBackupTimer.Start();
        }
    }

    private async void AutoProjectBackupTimer_Tick(object? sender, EventArgs e)
    {
        await EnsureAutomaticProjectBackupAsync();
    }

    private async Task EnsureAutomaticProjectBackupAsync()
    {
        if (_autoProjectBackupInProgress || !_backupService.CanCreateBackup)
        {
            return;
        }

        var backupDirectory = _backupService.GetProjectBackupDirectory();
        if (HasAutomaticProjectBackupForDate(backupDirectory, DateTime.Now))
        {
            return;
        }

        _autoProjectBackupInProgress = true;
        try
        {
            var result = await _backupService.CreateProjectBackupAsync(automatic: true);
            if (result.Succeeded)
            {
                FooterMessageTextBlock.Text = LanguageManager.CurrentLanguage == AppLanguage.Russian
                    ? $"Авто backup создан в папке проекта: {backupDirectory}"
                    : $"Automatic backup created in the project folder: {backupDirectory}";
            }
        }
        catch
        {
            // Automatic backups are best-effort and should not interrupt admin work.
        }
        finally
        {
            _autoProjectBackupInProgress = false;
        }
    }

    private static bool HasAutomaticProjectBackupForDate(string backupDirectory, DateTime targetDate)
    {
        if (!Directory.Exists(backupDirectory))
        {
            return false;
        }

        var expectedPrefix = $"{DatabaseSettings.Database}_auto_";
        return Directory.EnumerateFiles(backupDirectory, "*.sql", SearchOption.TopDirectoryOnly)
            .Any(path =>
            {
                var fileName = Path.GetFileName(path);
                return fileName.StartsWith(expectedPrefix, StringComparison.OrdinalIgnoreCase) &&
                       File.GetLastWriteTime(path).Date == targetDate.Date;
            });
    }

