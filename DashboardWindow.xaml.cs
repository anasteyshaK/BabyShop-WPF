    private void EnsureDashboardOrderDetailsButtonColumn(DataGrid dataGrid)
    {
        var existingColumn = dataGrid.Columns.FirstOrDefault(column =>
            string.Equals(column.SortMemberPath, DashboardOrderDetailsColumnSortKey, StringComparison.OrdinalIgnoreCase));

        if (existingColumn is not null)
        {
            dataGrid.Columns.Remove(existingColumn);
        }

        if (!dataGrid.Columns.Any(column =>
                string.Equals(column.SortMemberPath, "order_id", StringComparison.OrdinalIgnoreCase)))
        {
            return;
        }

        var iconPathFactory = new FrameworkElementFactory(typeof(Path));
        iconPathFactory.SetValue(Path.WidthProperty, 14d);
        iconPathFactory.SetValue(Path.HeightProperty, 16d);
        iconPathFactory.SetValue(Path.StretchProperty, Stretch.Uniform);
        iconPathFactory.SetValue(Path.StrokeProperty, (Brush)FindResource("AccentBrush"));
        iconPathFactory.SetValue(Path.FillProperty, Brushes.Transparent);
        iconPathFactory.SetValue(Path.StrokeThicknessProperty, 1.6d);
        iconPathFactory.SetValue(Path.StrokeStartLineCapProperty, PenLineCap.Round);
        iconPathFactory.SetValue(Path.StrokeEndLineCapProperty, PenLineCap.Round);
        iconPathFactory.SetValue(Path.StrokeLineJoinProperty, PenLineJoin.Round);
        iconPathFactory.SetValue(Path.DataProperty, Geometry.Parse("M4,1.5 L10.5,1.5 L14,5 L14,14.5 L4,14.5 Z M10.5,1.5 L10.5,5 L14,5 M6.5,7.5 L11.5,7.5 M6.5,10.5 L11.5,10.5"));

        var viewboxFactory = new FrameworkElementFactory(typeof(Viewbox));
        viewboxFactory.SetValue(FrameworkElement.HorizontalAlignmentProperty, HorizontalAlignment.Center);
        viewboxFactory.SetValue(FrameworkElement.VerticalAlignmentProperty, VerticalAlignment.Center);
        viewboxFactory.AppendChild(iconPathFactory);

        var buttonFactory = new FrameworkElementFactory(typeof(Button));
        buttonFactory.SetValue(FrameworkElement.WidthProperty, 38d);
        buttonFactory.SetValue(FrameworkElement.HeightProperty, 38d);
        buttonFactory.SetValue(FrameworkElement.MarginProperty, new Thickness(0));
        buttonFactory.SetValue(Control.PaddingProperty, new Thickness(0));
        buttonFactory.SetValue(Control.CursorProperty, Cursors.Hand);
        buttonFactory.SetValue(Control.ForegroundProperty, (Brush)FindResource("AccentBrush"));
        buttonFactory.SetValue(Control.BackgroundProperty, Brushes.Transparent);
        buttonFactory.SetValue(FrameworkElement.HorizontalAlignmentProperty, HorizontalAlignment.Center);
        buttonFactory.SetValue(FrameworkElement.VerticalAlignmentProperty, VerticalAlignment.Center);
        buttonFactory.SetValue(FrameworkElement.ToolTipProperty, LanguageManager.CurrentLanguage == AppLanguage.Russian ? "Детали" : "Details");
