        {
            dataGrid.Columns[^1].DisplayIndex = dataGrid.Columns.Count - 1;
        }
    }

    private static ControlTemplate BuildDashboardOrderDetailsButtonTemplate()
    {
        var borderFactory = new FrameworkElementFactory(typeof(Border));
        borderFactory.Name = "RootBorder";
        borderFactory.SetValue(Border.BackgroundProperty, Brushes.Transparent);
        borderFactory.SetValue(Border.BorderBrushProperty, Brushes.Transparent);
        borderFactory.SetValue(Border.BorderThicknessProperty, new Thickness(0));
        borderFactory.SetValue(Border.CornerRadiusProperty, new CornerRadius(0));

        var presenterFactory = new FrameworkElementFactory(typeof(ContentPresenter));
        presenterFactory.SetValue(HorizontalAlignmentProperty, HorizontalAlignment.Center);
        presenterFactory.SetValue(VerticalAlignmentProperty, VerticalAlignment.Center);
        borderFactory.AppendChild(presenterFactory);

        var template = new ControlTemplate(typeof(Button))
        {
            VisualTree = borderFactory
        };

        var hoverTrigger = new Trigger
        {
            Property = UIElement.IsMouseOverProperty,
            Value = true
        };
        hoverTrigger.Setters.Add(new Setter(UIElement.OpacityProperty, 0.84d, "RootBorder"));

        var pressedTrigger = new Trigger
        {
            Property = ButtonBase.IsPressedProperty,
            Value = true
        };
        pressedTrigger.Setters.Add(new Setter(UIElement.OpacityProperty, 0.68d, "RootBorder"));

        template.Triggers.Add(hoverTrigger);
        template.Triggers.Add(pressedTrigger);
        return template;
    }

    private void DashboardOrderDetailsButton_Click(object sender, RoutedEventArgs e)
    {
        if (_isBusy || sender is not Button button)
        {
            return;
        }

        var rawOrderId = button.Tag;
        if (rawOrderId is null || rawOrderId is DBNull)
        {
            MessageBox.Show(
                LanguageManager.CurrentLanguage == AppLanguage.Russian ? "Не удалось определить ID заказа." : "Could not determine the order ID.",
                Title,
                MessageBoxButton.OK,
                MessageBoxImage.Warning);
            return;
        }

        if (button.DataContext is DataRowView rowView)
        {
            DashboardDataGrid.SelectedItem = rowView;
        }

        var orderDetailsWindow = new OrderDetailsWindow(Convert.ToInt32(rawOrderId, CultureInfo.CurrentCulture))
        {
            Owner = this
        };

        orderDetailsWindow.ShowDialog();
    }

    private async void ApplyFiltersButton_Click(object sender, RoutedEventArgs e)
    {
        if (_isBusy)
        {
            return;
        }

        SetBusyState(true);
        try
        {
            await LoadDashboardAsync();
        }
        catch (Exception exception)
        {
            MessageBox.Show(exception.Message, LanguageManager.Get("Dashboard"), MessageBoxButton.OK, MessageBoxImage.Error);
        }
        finally
        {
            SetBusyState(false);
        }
    }

