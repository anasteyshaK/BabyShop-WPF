    private static Path CreatePieSlice(Point center, double radius, double startAngle, double sweepAngle, Brush fill)
    {
        var startRadians = Math.PI * startAngle / 180.0;
        var endRadians = Math.PI * (startAngle + sweepAngle) / 180.0;

        var startPoint = new Point(
            center.X + radius * Math.Cos(startRadians),
            center.Y + radius * Math.Sin(startRadians));

        var endPoint = new Point(
            center.X + radius * Math.Cos(endRadians),
            center.Y + radius * Math.Sin(endRadians));

        var geometry = new PathGeometry();
        var figure = new PathFigure
        {
            StartPoint = center,
            IsClosed = true
        };

        figure.Segments.Add(new LineSegment(startPoint, true));
        figure.Segments.Add(new ArcSegment(
            endPoint,
            new Size(radius, radius),
            0,
            sweepAngle > 180,
            SweepDirection.Clockwise,
            true));
        geometry.Figures.Add(figure);

        return new Path
        {
            Data = geometry,
            Fill = fill,
            Stroke = Brushes.White,
            StrokeThickness = 2
        };
    }

