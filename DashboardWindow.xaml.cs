    private void AmountThumb_MouseLeftButtonDown(object sender, MouseButtonEventArgs e)
    {
        if (_isBusy)
        {
            return;
        }

        _amountDragTarget = ReferenceEquals(sender, MinAmountThumb)
            ? AmountDragTarget.Minimum
            : AmountDragTarget.Maximum;
        AmountRangeCanvas.CaptureMouse();
        e.Handled = true;
    }

    private void AmountRangeCanvas_MouseLeftButtonDown(object sender, MouseButtonEventArgs e)
    {
        if (_isBusy)
        {
            return;
        }

        var clickValue = AmountFromCanvasX(e.GetPosition(AmountRangeCanvas).X);
        _amountDragTarget = Math.Abs(clickValue - _selectedMinAmount) <= Math.Abs(clickValue - _selectedMaxAmount)
            ? AmountDragTarget.Minimum
            : AmountDragTarget.Maximum;

        SetSelectedAmount(_amountDragTarget, clickValue, updateTextBoxes: true);
        AmountRangeCanvas.CaptureMouse();
    }

    private void AmountRangeCanvas_MouseMove(object sender, MouseEventArgs e)
    {
        if (_amountDragTarget == AmountDragTarget.None || !_amountRangeCanvasHasCapture())
        {
            return;
        }

        SetSelectedAmount(_amountDragTarget, AmountFromCanvasX(e.GetPosition(AmountRangeCanvas).X), updateTextBoxes: true);
    }

    private void AmountRangeCanvas_MouseLeftButtonUp(object sender, MouseButtonEventArgs e)
    {
        _amountDragTarget = AmountDragTarget.None;
        AmountRangeCanvas.ReleaseMouseCapture();
    }

    private void AmountTextBox_TextChanged(object sender, TextChangedEventArgs e)
    {
        if (_isUpdatingAmountControls || AmountRangeCanvas is null)
        {
            return;
        }

        _isUpdatingAmountControls = true;

        if (TryParseNullableDecimal(MinPriceTextBox.Text, out var minPrice) && minPrice.HasValue)
        {
            _selectedMinAmount = ClampToAmountRange(minPrice.Value);
        }

        if (TryParseNullableDecimal(MaxPriceTextBox.Text, out var maxPrice) && maxPrice.HasValue)
        {
            _selectedMaxAmount = ClampToAmountRange(maxPrice.Value);
        }

        if (_selectedMinAmount > _selectedMaxAmount)
        {
            if (ReferenceEquals(sender, MinPriceTextBox))
            {
                _selectedMaxAmount = _selectedMinAmount;
            }
            else
            {
                _selectedMinAmount = _selectedMaxAmount;
            }
        }

        UpdateAmountSliderLabels();
        UpdateAmountRangeVisual();
        _isUpdatingAmountControls = false;
    }

    private void SetSelectedAmount(AmountDragTarget target, decimal value, bool updateTextBoxes)
    {
        value = ClampToAmountRange(value);

        if (target == AmountDragTarget.Minimum)
        {
            _selectedMinAmount = Math.Min(value, _selectedMaxAmount);
        }
        else if (target == AmountDragTarget.Maximum)
        {
            _selectedMaxAmount = Math.Max(value, _selectedMinAmount);
        }

        _isUpdatingAmountControls = true;
        if (updateTextBoxes)
        {
            MinPriceTextBox.Text = FormatMoney(_selectedMinAmount);
            MaxPriceTextBox.Text = FormatMoney(_selectedMaxAmount);
        }

        UpdateAmountSliderLabels();
        UpdateAmountRangeVisual();
        _isUpdatingAmountControls = false;
    }

    private decimal ClampToAmountRange(decimal value)
    {
        return Math.Min(_amountSliderMaximum, Math.Max(_amountSliderMinimum, value));
    }

    private void UpdateAmountSliderLabels()
    {
        MinAmountSliderValueTextBlock.Text = FormatMoney(_selectedMinAmount);
        MaxAmountSliderValueTextBlock.Text = FormatMoney(_selectedMaxAmount);
    }

    private void AmountRangeCanvas_SizeChanged(object sender, SizeChangedEventArgs e)
    {
        UpdateAmountRangeVisual();
    }

    private void UpdateAmountRangeVisual()
    {
        if (AmountRangeCanvas is null || AmountTrackBorder is null || AmountSelectedRangeBorder is null)
        {
            return;
        }

        var canvasWidth = AmountRangeCanvas.ActualWidth;
        if (canvasWidth <= 22)
        {
            return;
        }

        const double thumbSize = 20;
        var trackLeft = thumbSize / 2;
        var trackTop = 15d;
        var trackWidth = Math.Max(1, canvasWidth - thumbSize);

        AmountTrackBorder.Width = trackWidth;
        Canvas.SetLeft(AmountTrackBorder, trackLeft);
        Canvas.SetTop(AmountTrackBorder, trackTop);

        var minX = AmountToCanvasX(_selectedMinAmount, trackLeft, trackWidth);
        var maxX = AmountToCanvasX(_selectedMaxAmount, trackLeft, trackWidth);

        AmountSelectedRangeBorder.Width = Math.Max(0, maxX - minX);
        Canvas.SetLeft(AmountSelectedRangeBorder, minX);
        Canvas.SetTop(AmountSelectedRangeBorder, trackTop);

        Canvas.SetLeft(MinAmountThumb, minX - thumbSize / 2);
        Canvas.SetLeft(MaxAmountThumb, maxX - thumbSize / 2);
    }

    private double AmountToCanvasX(decimal value, double trackLeft, double trackWidth)
    {
        var range = _amountSliderMaximum - _amountSliderMinimum;
        if (range <= 0)
        {
            return trackLeft;
        }

        var ratio = (double)((value - _amountSliderMinimum) / range);
        return trackLeft + ratio * trackWidth;
    }

    private decimal AmountFromCanvasX(double x)
    {
        const double thumbSize = 20;
        var trackLeft = thumbSize / 2;
        var trackWidth = Math.Max(1, AmountRangeCanvas.ActualWidth - thumbSize);
        var ratio = Math.Min(1d, Math.Max(0d, (x - trackLeft) / trackWidth));
        return _amountSliderMinimum + (_amountSliderMaximum - _amountSliderMinimum) * (decimal)ratio;
    }

    private bool _amountRangeCanvasHasCapture()
    {
        return Mouse.Captured == AmountRangeCanvas;
    }

    private void RenderBarChart(Panel panel, IReadOnlyList<DashboardSeriesPoint> points, Brush accentBrush, string emptyMessageKey)
    {
        panel.Children.Clear();

        if (points.Count == 0)
        {
            panel.Children.Add(CreateEmptyState(LanguageManager.Get(emptyMessageKey)));
            return;
        }

        var maxValue = points.Max(point => point.Value);
        if (maxValue <= 0)
        {
            maxValue = 1;
        }

        var barMaxWidth = Math.Max(120, panel.ActualWidth - 240);
        if (double.IsNaN(barMaxWidth) || double.IsInfinity(barMaxWidth))
        {
            barMaxWidth = 180;
        }

        foreach (var point in points)
        {
            var row = new Grid
            {
                Margin = new Thickness(0, 0, 0, 12)
            };

            row.ColumnDefinitions.Add(new ColumnDefinition { Width = new GridLength(135) });
            row.ColumnDefinitions.Add(new ColumnDefinition());
            row.ColumnDefinitions.Add(new ColumnDefinition { Width = GridLength.Auto });

            var label = new TextBlock
            {
                Text = point.Label,
                VerticalAlignment = VerticalAlignment.Center,
                TextTrimming = TextTrimming.CharacterEllipsis,
                Margin = new Thickness(0, 0, 12, 0)
            };

            var barTrack = new Border
            {
                Height = 18,
                CornerRadius = new CornerRadius(9),
                Background = new SolidColorBrush(Color.FromRgb(255, 231, 238)),
                VerticalAlignment = VerticalAlignment.Center
            };

            var barFill = new Border
            {
                Height = 18,
                Width = barMaxWidth * (double)(point.Value / maxValue),
                CornerRadius = new CornerRadius(9),
                Background = accentBrush,
                HorizontalAlignment = HorizontalAlignment.Left
            };

            var barGrid = new Grid();
            barGrid.Children.Add(barTrack);
            barGrid.Children.Add(barFill);

            var value = new TextBlock
            {
                Text = FormatMoney(point.Value),
                Margin = new Thickness(12, 0, 0, 0),
                VerticalAlignment = VerticalAlignment.Center,
                FontWeight = FontWeights.SemiBold,
                MinWidth = 72,
                TextAlignment = TextAlignment.Right
            };

            Grid.SetColumn(label, 0);
            Grid.SetColumn(barGrid, 1);
            Grid.SetColumn(value, 2);

            row.Children.Add(label);
            row.Children.Add(barGrid);
            row.Children.Add(value);

            panel.Children.Add(row);
        }
    }

    private void RenderStatusPieChart(IReadOnlyList<DashboardSeriesPoint> points)
    {
        StatusPieCanvas.Children.Clear();
        StatusLegendPanel.Children.Clear();

        if (points.Count == 0)
        {
            StatusLegendPanel.Children.Add(CreateEmptyState(LanguageManager.Get("DashboardNoStatusData")));
            return;
        }

        var total = points.Sum(point => point.Value);
        if (total <= 0)
        {
            StatusLegendPanel.Children.Add(CreateEmptyState(LanguageManager.Get("DashboardNoStatusData")));
            return;
        }

        const double size = 220;
        const double radius = size / 2;
        var center = new Point(radius, radius);
        double startAngle = 0;

        StatusPieCanvas.Children.Add(new Ellipse
        {
            Width = size,
            Height = size,
            Fill = new SolidColorBrush(Color.FromRgb(255, 251, 246))
        });

        for (var index = 0; index < points.Count; index++)
        {
            var point = points[index];
            var sweepAngle = 360d * (double)(point.Value / total);
            var slice = CreatePieSlice(center, radius, startAngle, sweepAngle, ChartBrushes[index % ChartBrushes.Length]);
            StatusPieCanvas.Children.Add(slice);

            var percentage = point.Value / total;
            var legendRow = new StackPanel
            {
                Orientation = Orientation.Horizontal,
                Margin = new Thickness(0, 0, 0, 10)
            };

            legendRow.Children.Add(new Border
            {
                Width = 14,
                Height = 14,
                CornerRadius = new CornerRadius(7),
                Background = ChartBrushes[index % ChartBrushes.Length],
                Margin = new Thickness(0, 2, 10, 0)
            });

            legendRow.Children.Add(new TextBlock
            {
                Text = $"{FormatStatusLabel(point.Label)} ({percentage:P0})",
                FontWeight = FontWeights.SemiBold
            });

            legendRow.Children.Add(new TextBlock
            {
                Margin = new Thickness(8, 0, 0, 0),
                Text = $"{LanguageManager.Get("DashboardOrdersShort")}: {point.Value:N0}",
                Foreground = (Brush)FindResource("MutedTextBrush")
            });

            StatusLegendPanel.Children.Add(legendRow);
            startAngle += sweepAngle;
        }

        StatusPieCanvas.Children.Add(new Ellipse
        {
            Width = 84,
            Height = 84,
            Fill = Brushes.White,
            Stroke = new SolidColorBrush(Color.FromRgb(232, 222, 212)),
            StrokeThickness = 1.5
        });
        Canvas.SetLeft(StatusPieCanvas.Children[^1], 68);
        Canvas.SetTop(StatusPieCanvas.Children[^1], 68);

        var totalText = new TextBlock
        {
            Width = 84,
            TextAlignment = TextAlignment.Center,
            FontWeight = FontWeights.Bold,
            FontSize = 12,
            Foreground = (Brush)FindResource("TextBrush"),
            Text = $"{LanguageManager.Get("DashboardOrdersShort")}\n{total:N0}"
        };
        StatusPieCanvas.Children.Add(totalText);
        Canvas.SetLeft(totalText, 68);
        Canvas.SetTop(totalText, 94);
    }

    private void RenderTimelineChart(IReadOnlyList<DashboardSeriesPoint> points)
    {
        TimelineChartCanvas.Children.Clear();

        if (points.Count == 0)
        {
            TimelineChartCanvas.Children.Add(CreateCanvasPlaceholder(LanguageManager.Get("DashboardNoTimelineData")));
            return;
        }

        var width = Math.Max(TimelineChartCanvas.ActualWidth, 520);
        var height = Math.Max(TimelineChartCanvas.ActualHeight, 240);
        const double left = 48;
        const double top = 18;
        const double bottom = 36;
        const double right = 18;
        var plotWidth = width - left - right;
        var plotHeight = height - top - bottom;

        var maxValue = points.Max(point => point.Value);
        if (maxValue <= 0)
        {
            maxValue = 1;
        }

        for (var guideIndex = 0; guideIndex < 4; guideIndex++)
        {
            var guideY = top + (plotHeight / 3 * guideIndex);
            TimelineChartCanvas.Children.Add(new Line
            {
                X1 = left,
                Y1 = guideY,
                X2 = left + plotWidth,
                Y2 = guideY,
                Stroke = new SolidColorBrush(Color.FromRgb(232, 222, 212)),
                StrokeThickness = 1
            });
        }

        TimelineChartCanvas.Children.Add(new Line
        {
            X1 = left,
            Y1 = top,
            X2 = left,
            Y2 = top + plotHeight,
            Stroke = new SolidColorBrush(Color.FromRgb(218, 205, 193)),
            StrokeThickness = 1
        });

        TimelineChartCanvas.Children.Add(new Line
        {
            X1 = left,
            Y1 = top + plotHeight,
            X2 = left + plotWidth,
            Y2 = top + plotHeight,
            Stroke = new SolidColorBrush(Color.FromRgb(218, 205, 193)),
            StrokeThickness = 1
        });

        var polyline = new Polyline
        {
            Stroke = ChartBrushes[2],
            StrokeThickness = 3
        };

        for (var index = 0; index < points.Count; index++)
        {
            var x = left + (points.Count == 1 ? plotWidth / 2 : index * (plotWidth / (points.Count - 1)));
            var ratio = (double)(points[index].Value / maxValue);
            var y = top + plotHeight - (plotHeight * ratio);

            polyline.Points.Add(new Point(x, y));

            var dot = new Ellipse
            {
                Width = 10,
                Height = 10,
                Fill = ChartBrushes[0],
                Stroke = Brushes.White,
                StrokeThickness = 2
            };

            TimelineChartCanvas.Children.Add(dot);
            Canvas.SetLeft(dot, x - 5);
            Canvas.SetTop(dot, y - 5);

            if (index == 0 || index == points.Count - 1 || points.Count <= 6 || index % 2 == 0)
            {
                var label = new TextBlock
                {
                    Text = points[index].Label,
                    FontSize = 11,
                    Foreground = (Brush)FindResource("MutedTextBrush")
                };
                TimelineChartCanvas.Children.Add(label);
                Canvas.SetLeft(label, Math.Max(0, x - 16));
                Canvas.SetTop(label, top + plotHeight + 6);
            }
        }

        var maxLabel = new TextBlock
        {
            Text = FormatMoney(maxValue),
            FontSize = 11,
            Foreground = (Brush)FindResource("MutedTextBrush")
        };

        TimelineChartCanvas.Children.Add(maxLabel);
        Canvas.SetLeft(maxLabel, 0);
        Canvas.SetTop(maxLabel, top - 8);
        TimelineChartCanvas.Children.Add(polyline);
    }

    private void RenderMonthHistogram(IReadOnlyList<DashboardSeriesPoint> points)
    {
        MonthHistogramCanvas.Children.Clear();

        if (points.Count == 0)
        {
            MonthHistogramCanvas.Children.Add(CreateCanvasPlaceholder(LanguageManager.Get("DashboardNoMonthData")));
            return;
        }

        var requiredWidth = Math.Max(420, points.Count * 86 + 78);
        var width = Math.Max(MonthHistogramCanvas.ActualWidth, requiredWidth);
        var height = Math.Max(MonthHistogramCanvas.ActualHeight, 210);
        MonthHistogramCanvas.Width = width;

        const double left = 54;
        const double top = 20;
        const double bottom = 42;
        const double right = 18;
        var plotWidth = width - left - right;
        var plotHeight = height - top - bottom;

        var maxValue = points.Max(point => point.Value);
        if (maxValue <= 0)
        {
            maxValue = 1;
        }

        for (var guideIndex = 0; guideIndex < 4; guideIndex++)
        {
            var guideY = top + (plotHeight / 3 * guideIndex);
            MonthHistogramCanvas.Children.Add(new Line
            {
                X1 = left,
                Y1 = guideY,
                X2 = left + plotWidth,
                Y2 = guideY,
                Stroke = new SolidColorBrush(Color.FromRgb(232, 222, 212)),
                StrokeThickness = 1
            });
        }

        MonthHistogramCanvas.Children.Add(new Line
        {
            X1 = left,
            Y1 = top,
            X2 = left,
            Y2 = top + plotHeight,
            Stroke = new SolidColorBrush(Color.FromRgb(218, 205, 193)),
            StrokeThickness = 1
        });

        MonthHistogramCanvas.Children.Add(new Line
        {
            X1 = left,
            Y1 = top + plotHeight,
            X2 = left + plotWidth,
            Y2 = top + plotHeight,
            Stroke = new SolidColorBrush(Color.FromRgb(218, 205, 193)),
            StrokeThickness = 1
        });

        var maxLabel = new TextBlock
        {
            Text = FormatMoney(maxValue),
            FontSize = 11,
            Foreground = (Brush)FindResource("MutedTextBrush")
        };
        MonthHistogramCanvas.Children.Add(maxLabel);
        Canvas.SetLeft(maxLabel, 0);
        Canvas.SetTop(maxLabel, top - 8);

        var slotWidth = plotWidth / points.Count;
        var barWidth = Math.Min(54, Math.Max(30, slotWidth * 0.58));

        for (var index = 0; index < points.Count; index++)
        {
            var point = points[index];
            var ratio = (double)(point.Value / maxValue);
            var barHeight = Math.Max(4, plotHeight * ratio);
            var x = left + index * slotWidth + (slotWidth - barWidth) / 2;
            var y = top + plotHeight - barHeight;

            var bar = new Rectangle
            {
                Width = barWidth,
                Height = barHeight,
                RadiusX = 4,
                RadiusY = 4,
                Fill = ChartBrushes[index % ChartBrushes.Length]
            };
            MonthHistogramCanvas.Children.Add(bar);
            Canvas.SetLeft(bar, x);
            Canvas.SetTop(bar, y);

            var valueLabel = new TextBlock
            {
                Text = FormatMoney(point.Value),
                FontSize = 11,
