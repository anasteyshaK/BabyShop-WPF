    private async void Window_Loaded(object sender, RoutedEventArgs e)
    {
        await InitializeAsync();
    }

    private async Task InitializeAsync()
    {
        SetBusyState(true);

        try
        {
            await LoadFilterOptionsAsync();
            await LoadAmountFilterBoundsAsync();
            await LoadDashboardAsync();
        }
        catch (Exception exception)
        {
            MessageBox.Show(exception.Message, LanguageManager.Get("Dashboard"), MessageBoxButton.OK, MessageBoxImage.Error);
            DashboardStatusTextBlock.Text = exception.Message;
        }
        finally
        {
            SetBusyState(false);
        }
    }

    private async Task LoadFilterOptionsAsync()
    {
        var clientsTask = _repository.GetDashboardLookupOptionsAsync("c_fullname");
        var productsTask = _repository.GetDashboardLookupOptionsAsync("product_title");
        var fabricsTask = _repository.GetDashboardLookupOptionsAsync("fabric_type");

        await Task.WhenAll(clientsTask, productsTask, fabricsTask);

        _clientOptions = clientsTask.Result.ToList();
        _productOptions = productsTask.Result.ToList();
        _fabricOptions = fabricsTask.Result.ToList();

        RefreshFilterItems();
    }

    private async Task LoadAmountFilterBoundsAsync()
    {
        var bounds = await _repository.GetDashboardAmountBoundsAsync();
        ConfigureAmountSliders(bounds.Minimum, bounds.Maximum);
    }

    private void ConfigureAmountSliders(decimal minimum, decimal maximum)
    {
        if (maximum <= minimum)
        {
            maximum = minimum + 1m;
        }

        _amountSliderMinimum = minimum;
        _amountSliderMaximum = maximum;
        _selectedMinAmount = minimum;
        _selectedMaxAmount = maximum;

        _isUpdatingAmountControls = true;
        MinPriceTextBox.Text = FormatMoney(minimum);
        MaxPriceTextBox.Text = FormatMoney(maximum);
        UpdateAmountSliderLabels();
        UpdateAmountRangeVisual();
        _isUpdatingAmountControls = false;
    }

    private void RefreshFilterItems()
    {
        var selectedClient = ClientComboBox.SelectedValue as string;
        var selectedStatus = StatusComboBox.SelectedValue as string;
        var selectedProduct = ProductComboBox.SelectedValue as string;
        var selectedFabric = FabricTypeComboBox.SelectedValue as string;

        ClientComboBox.ItemsSource = BuildOptionsWithAll(_clientOptions, "AllClients");
        ProductComboBox.ItemsSource = BuildOptionsWithAll(_productOptions, "AllProducts");
        FabricTypeComboBox.ItemsSource = BuildOptionsWithAll(_fabricOptions, "AllFabricTypes");
        StatusComboBox.ItemsSource = BuildStatusOptions();

        ClientComboBox.SelectedValue = selectedClient;
        StatusComboBox.SelectedValue = selectedStatus;
        ProductComboBox.SelectedValue = selectedProduct;
        FabricTypeComboBox.SelectedValue = selectedFabric;
    }

    private async Task LoadDashboardAsync()
    {
        var filter = ReadFilter();
        var hasStatusFilter = !string.IsNullOrWhiteSpace(filter.Status);

        var dataTask = _repository.GetDashboardDataAsync(filter);
        var summaryTask = _repository.GetDashboardSummaryAsync(filter);
        var statusTask = hasStatusFilter
            ? Task.FromResult<IReadOnlyList<DashboardSeriesPoint>>([])
            : _repository.GetDashboardStatusChartAsync(filter);
        var categoryTask = _repository.GetDashboardCategoryChartAsync(filter);
        var timelineTask = _repository.GetDashboardTimelineChartAsync(filter);
        var monthTask = _repository.GetDashboardMonthStatsAsync(filter);

        await Task.WhenAll(dataTask, summaryTask, statusTask, categoryTask, timelineTask, monthTask);

        DashboardDataGrid.ItemsSource = dataTask.Result.DefaultView;
        ApplyDashboardSummary(summaryTask.Result);

        _statusPoints = statusTask.Result;
        _categoryPoints = categoryTask.Result;
        _timelinePoints = timelineTask.Result;
        _monthPoints = monthTask.Result;

        UpdateChartLayout(hasStatusFilter);
        if (!hasStatusFilter)
        {
            RenderStatusPieChart(_statusPoints);
        }
        else
        {
            StatusPieCanvas.Children.Clear();
            StatusLegendPanel.Children.Clear();
        }

        RenderBarChart(ProductChartPanel, _categoryPoints, ChartBrushes[0], "DashboardNoCategoryData");
        RenderTimelineChart(_timelinePoints);
        RenderMonthHistogram(_monthPoints);

        DashboardStatusTextBlock.Text = LanguageManager.Format("DashboardRowsLoaded", dataTask.Result.Rows.Count);
    }

    private DashboardFilter ReadFilter()
    {
        var dateFrom = DateFromPicker.SelectedDate;
        var dateTo = DateToPicker.SelectedDate;

        if (dateFrom.HasValue && dateTo.HasValue && dateFrom > dateTo)
        {
            throw new InvalidOperationException(LanguageManager.Get("DashboardDateRangeError"));
        }

        var minPrice = ParseNullableDecimal(MinPriceTextBox.Text, "DashboardMinAmount");
        var maxPrice = ParseNullableDecimal(MaxPriceTextBox.Text, "DashboardMaxAmount");
        if (minPrice.HasValue && maxPrice.HasValue && minPrice > maxPrice)
        {
            throw new InvalidOperationException(LanguageManager.Get("DashboardAmountRangeError"));
        }

        return new DashboardFilter
        {
            DateFrom = dateFrom,
            DateTo = dateTo,
            Status = ReadSelectedValue(StatusComboBox),
            ClientName = ReadSelectedValue(ClientComboBox),
            ProductTitle = ReadSelectedValue(ProductComboBox),
            FabricType = ReadSelectedValue(FabricTypeComboBox),
            MinPrice = minPrice,
            MaxPrice = maxPrice
        };
    }

    private static string? ReadSelectedValue(ComboBox comboBox)
    {
        var value = comboBox.SelectedValue as string;
        return string.IsNullOrWhiteSpace(value) ? null : value;
    }

    private static decimal? ParseNullableDecimal(string text, string fieldKey)
    {
        if (string.IsNullOrWhiteSpace(text))
        {
            return null;
        }

        if (decimal.TryParse(text, NumberStyles.Number, CultureInfo.CurrentCulture, out var currentValue))
        {
            return currentValue;
        }

        if (decimal.TryParse(text, NumberStyles.Number, CultureInfo.InvariantCulture, out var invariantValue))
        {
            return invariantValue;
        }

        throw new InvalidOperationException(LanguageManager.Format("NumericValidation", LanguageManager.Get(fieldKey)));
    }

    private static bool TryParseNullableDecimal(string text, out decimal? value)
    {
        value = null;
        if (string.IsNullOrWhiteSpace(text))
        {
            return true;
        }

        if (decimal.TryParse(text, NumberStyles.Number, CultureInfo.CurrentCulture, out var currentValue))
        {
            value = currentValue;
            return true;
        }

        if (decimal.TryParse(text, NumberStyles.Number, CultureInfo.InvariantCulture, out var invariantValue))
        {
            value = invariantValue;
            return true;
        }

        return false;
    }

    private void ApplyDashboardSummary(DashboardSummary summary)
    {
        TotalRevenueValueTextBlock.Text = FormatMoney(summary.TotalSum);
        OrdersCountValueTextBlock.Text = summary.OrderCount.ToString("N0", CultureInfo.CurrentCulture);
