    private void DashboardDataGrid_AutoGeneratingColumn(object? sender, DataGridAutoGeneratingColumnEventArgs e)
    {
        e.Column.Header = LanguageManager.GetColumnName(e.PropertyName) ?? e.PropertyName;

        if (e.PropertyName.Equals("order_id", StringComparison.OrdinalIgnoreCase))
        {
            e.Column.Header = LanguageManager.CurrentLanguage == AppLanguage.Russian ? "Номер заказа" : "Order Number";
        }

        if (e.PropertyName.Equals("order_items", StringComparison.OrdinalIgnoreCase))
        {
            e.Cancel = true;
            return;
        }

        if (e.PropertyName.Equals("start_date", StringComparison.OrdinalIgnoreCase) ||
            e.PropertyName.Equals("end_date", StringComparison.OrdinalIgnoreCase))
        {
            if (e.Column is DataGridTextColumn textColumn)
            {
                textColumn.Binding.StringFormat = "dd.MM.yyyy";
            }
        }

        if (e.PropertyName.Equals("total_cost", StringComparison.OrdinalIgnoreCase) ||
            e.PropertyName.Equals("line_total", StringComparison.OrdinalIgnoreCase) ||
            e.PropertyName.Equals("price_per_m", StringComparison.OrdinalIgnoreCase) ||
            e.PropertyName.Equals("fabric_amount", StringComparison.OrdinalIgnoreCase))
        {
            if (e.Column is DataGridTextColumn numericColumn)
            {
                numericColumn.Binding.StringFormat = "N2";
            }
        }
    }

